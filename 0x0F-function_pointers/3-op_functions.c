#include "3-calc.h"

/**
 * op_add - Addition
 * @a: 1st value
 * @b: 2nd valude
 * Return: Result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Substraction
 * @a: 1st value
 * @b: 2nd valude
 * Return: Result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplication
 * @a: 1st value
 * @b: 2nd valude
 * Return: Result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Division
 * @a: 1st value
 * @b: 2nd valude
 * Return: Result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Modulo
 * @a: 1st value
 * @b: 2nd valude
 * Return: Result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
